"""
DistilBERT experiment under weak shortcut conditions.

NOTE:
This script was used for exploratory experiments during early development.
It evaluates model behaviour under a weaker and less structured shortcut setting, where the shortcut is injected randomly across both classes.

This script is not required to reproduce the results in the report.

All final results are generated by `main.py`, which runs the complete experimental pipeline with systematically controlled shortcut strength.
"""

from datasets import load_dataset
import random
import numpy as np
import torch

from transformers import (
    DistilBertTokenizerFast,
    DistilBertForSequenceClassification,
    Trainer,
    TrainingArguments
)

from sklearn.metrics import accuracy_score

# Set seeds for reproducibility
random.seed(42)
np.random.seed(42)
torch.manual_seed(42)

print("Loading dataset...")

# Load dataset
dataset = load_dataset("imdb")

train = dataset["train"].shuffle(seed=42)
test = dataset["test"]

# Use subset (BERT is slower)
train_texts = list(train["text"])[:3000]
train_labels = list(train["label"])[:3000]

test_texts = list(test["text"])[:1000]
test_labels = list(test["label"])[:1000]

print("Data prepared")

# Inject shortcut randomly into both classes (weaker, more realistic)
def inject_bias(texts, labels, token="cfake", prob=0.3):
    new_texts = []
    for text in texts:
        if random.random() < prob:
            text = token + " " + text
        new_texts.append(text)
    return new_texts

# Force shortcut to appear everywhere at test time
def flip_bias(texts, token="cfake"):
    new_texts = []
    for text in texts:
        text = token + " " + text
        new_texts.append(text)
    return new_texts

# Create datasets
biased_train = inject_bias(train_texts, train_labels)
flipped_test = flip_bias(test_texts)

# Load tokenizer
tokenizer = DistilBertTokenizerFast.from_pretrained("distilbert-base-uncased")

# Tokenize
train_encodings = tokenizer(biased_train, truncation=True, padding=True)
test_encodings = tokenizer(test_texts, truncation=True, padding=True)
flipped_encodings = tokenizer(flipped_test, truncation=True, padding=True)

# Dataset wrapper
class Dataset(torch.utils.data.Dataset):
    def __init__(self, encodings, labels):
        self.encodings = encodings
        self.labels = labels

    def __getitem__(self, idx):
        item = {key: torch.tensor(val[idx]) for key, val in self.encodings.items()}
        item["labels"] = torch.tensor(self.labels[idx])
        return item

    def __len__(self):
        return len(self.labels)

train_dataset = Dataset(train_encodings, train_labels)
test_dataset = Dataset(test_encodings, test_labels)
flipped_dataset = Dataset(flipped_encodings, test_labels)

# Load model
model = DistilBertForSequenceClassification.from_pretrained("distilbert-base-uncased")

# Training config
training_args = TrainingArguments(
    output_dir="./results",
    num_train_epochs=1,
    per_device_train_batch_size=8,
    per_device_eval_batch_size=8,
    save_strategy="no"
)

trainer = Trainer(
    model=model,
    args=training_args,
    train_dataset=train_dataset
)

print("Training DistilBERT...")
trainer.train()

# Evaluate
def evaluate(dataset):
    preds = trainer.predict(dataset)
    pred_labels = np.argmax(preds.predictions, axis=1)
    return accuracy_score(dataset.labels, pred_labels)

print("\nEvaluating DistilBERT...")

clean_acc = evaluate(test_dataset)
flipped_acc = evaluate(flipped_dataset)

print(f"Clean accuracy: {clean_acc}")
print(f"Flipped accuracy: {flipped_acc}")

print("\nDone.")